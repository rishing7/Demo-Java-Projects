
public class MathematicalOperation {
	public static int sum(int a, int b){
		System.out.println(a+b);
		return a+b;
	}
	public static int sub(int a, int b){
		System.out.println(a-b);
		return a-b;
	}
	public static int mul(int a, int b){
		System.out.println(a*b);
		return a*b;
	}
	public static int division(int a, int b){
		System.out.println(a/b);
		return a/b;
	}
}
